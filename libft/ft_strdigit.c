#include "libft.h"

char	*ft_strdigit(const char *s)
{
	if (s == NULL)
		return (NULL);
	while (*s != '\0')
	{
		if (48 <= *s && *s <= 57)
			return ((char *)s);
		s++;
	}
	return (NULL);
}

